<template>
  <div class="login-container">
    <el-form
      ref="loginForm"
      :model="loginForm"
      :rules="loginRules"
      class="login-form"
      autocomplete="on"
      label-position="left"
    >
      <div class="title-container">
          <h3 class="title">Подтверждение восстановления пароля</h3>
      </div>
      <el-form-item prop="code">
        <span class="svg-container">
          <i class="el-icon-code" ></i>
        </span>
        <el-input
          ref="code"
          v-model="loginForm.code"
          placeholder="Код подтвержденгия"
          name="code"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-button
        :loading="loading"
        type="primary"
        style="width:100%;margin-bottom:30px;"
        @click.native.prevent="login"
        >Подтвердить</el-button
      >

    </el-form>
  </div>
</template>

<script>
export default {
  name: "Login",
  components: { },
  data() {
    return {
      loginForm: {
        code: "",
      },
      loginRules: {
          code: [{ required: true, message: 'Пожалуйста, введите имя пользователя ', trigger: 'blur' }],
      },
      passwordType: "password",
      loading: false,
      redirect: undefined,
      otherQuery: {}
    };
  },
  methods: {
    showPwd() {
      if (this.passwordType === "password") {
        this.passwordType = "";
      } else {
        this.passwordType = "password";
      }
      this.$nextTick(() => {
        this.$refs.password.focus();
      });
    },
    login() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true;
          this.$store
            .dispatch("user/login", this.loginForm)
            .then(() => {
              this.$router.push({
                path: this.redirect || "/",
                query: this.otherQuery
              });
              this.loading = false;
            })
            .catch(() => {
              this.loading = false;
            });
        } else {
          return false;
        }
      });
    },
  }
};
</script>
